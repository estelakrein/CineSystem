    public void popularTabela() {

        List<Sala> resultado = new ArrayList();
        String sql = "FROM Sala "
                + "ORDER BY codigo";
        tblSalas.getColumnModel().getColumn(0).setPreferredWidth(50);
        tblSalas.getColumnModel().getColumn(1).setPreferredWidth(150);
        tblSalas.getColumnModel().getColumn(2).setPreferredWidth(30);
        DefaultTableModel modelo = (DefaultTableModel) tblSalas.getModel();
        modelo.setNumRows(0);
        try {
            Session sessao = HibernateUtil.getSessionFactory().openSession();
            org.hibernate.Query query = sessao.createQuery(sql);
            resultado = query.list();
            for (int i = 0; i < resultado.size(); i++) {
                Sala sala = resultado.get(i);
                modelo.addRow(new Object[]{sala.getCodigo(),
                    sala.getDescricao(), sala.getCodigo()});

            }
        } catch (HibernateException hibEx) {
            hibEx.printStackTrace();
        }

        // permite seleção de apenas uma linha da tabela
        tblSalas.setSelectionMode(0);
    }

