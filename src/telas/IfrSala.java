    private void btnIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirActionPerformed
        if (tfdFileira.getText().isEmpty() || tfdNumero.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Fileira e Número devem estar preenchidos!");
            tfdFileira.requestFocus();
        } else {
            Vector dadosTabela = new Vector();
            dadosTabela.add(tfdFileira.getText());
            dadosTabela.add(tfdNumero.getText());
            DefaultTableModel modelo = (DefaultTableModel) tblPoltronas.getModel();
            modelo.addRow(dadosTabela);
            tblPoltronas.setModel(modelo);
            tfdFileira.setText("");
            tfdNumero.setText("");
            tfdFileira.requestFocus();
        }
    }//GEN-LAST:event_btnIncluirActionPerformed


