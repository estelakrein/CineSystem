    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        List resultado = null;
        Session sessao = null;
        try {
            sessao = HibernateUtil.getSessionFactory().openSession();
            Transaction transacao = sessao.beginTransaction();
            String id = String.valueOf(tblSalas.getValueAt(tblSalas.getSelectedRow(), 0));

            org.hibernate.Query query = sessao.createQuery("FROM Poltrona WHERE codSala = " + id);

            resultado = query.list();
            for (Object obj : resultado) {
                Poltrona poltrona = (Poltrona) obj;
                sessao.delete(poltrona);
                transacao.commit();
                JOptionPane.showMessageDialog(null, "Poltronas excluídas com sucesso!");
            }
        } catch (HibernateException hibEx) {
            hibEx.printStackTrace();
        }
        resultado = null;
        sessao = null;
        try {
            sessao = HibernateUtil.getSessionFactory().openSession();
            Transaction transacao = sessao.beginTransaction();
            String id = String.valueOf(tblSalas.getValueAt(tblSalas.getSelectedRow(), 0));

            org.hibernate.Query query = sessao.createQuery("FROM Sala WHERE codigo = " + id);

            resultado = query.list();
            for (Object obj : resultado) {
                Sala sala = (Sala) obj;
                sessao.delete(sala);
                transacao.commit();
                JOptionPane.showMessageDialog(null, "Sala excluída com sucesso!");
            }
        } catch (HibernateException hibEx) {
            hibEx.printStackTrace();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            Sala sala = new Sala();
            sala.setDescricao(tfdDescricao.getText());
            DaoGenerico.getInstance().insert(sala);
            List resultado = null;
            Session sessao = null;
            try {
                sessao = HibernateUtil.getSessionFactory().openSession();
                Transaction transacao = sessao.beginTransaction();

                org.hibernate.Query query = sessao.createQuery("FROM Sala WHERE descricao = '" + tfdDescricao.getText() + "'");

                resultado = query.list();
                for (Object obj : resultado) {
                    sala = (Sala) obj;
                    codigo = sala.getCodigo();
                }
            } catch (HibernateException hibEx) {
                hibEx.printStackTrace();
            }
            for (int linha = 0; linha < tblPoltronas.getRowCount(); linha++) {
                Poltrona poltrona = new Poltrona();
                String fileira = String.valueOf(tblPoltronas.getValueAt(linha, 0));
                poltrona.setFileira(fileira);
                String numero = String.valueOf(tblPoltronas.getValueAt(linha, 1));
                poltrona.setNumero(Integer.parseInt(numero));
                poltrona.setSala(codigo);
                DaoGenerico.getInstance().insert(poltrona);
            }
            JOptionPane.showMessageDialog(null, "Cadastro finalizado com sucesso!");
            inicia();
        } catch (HibernateException hibEx) {
            hibEx.printStackTrace();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

