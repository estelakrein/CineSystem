    private void btnVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarActionPerformed
        // chamada de relatório, COM parâmetros
            try {
                // Compila o relatorio
                JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("../relatorios/RelVenda.jrxml"));

                // Mapeia campos de parametros para o relatorio, mesmo que nao existam
                Map parametros = new HashMap();

                // adiciona parametros
                if ((!Formatacao.removerFormatacao(tffDataInicial.getText()).isEmpty()) & (!Formatacao.removerFormatacao(tffDataFinal.getText()).isEmpty())) {
                    parametros.put("dataIni", Formatacao.ajustaDataAMD(tffDataInicial.getText()));
                    parametros.put("dataFin", Formatacao.ajustaDataAMD(tffDataFinal.getText()));
                }

                if (!tfdFilme.getText().isEmpty()) {
                    parametros.put("filme", Integer.parseInt(tfdFilme.getText()));
                }

                // Executa relatoio
                JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros);

                // Exibe resultado em video
                JasperViewer.viewReport(impressao, false);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao gerar relatório: " + e);
            }
    }//GEN-LAST:event_btnVisualizarActionPerformed

